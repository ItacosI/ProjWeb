<p>

Antonio, un riche marchand de Venise, décide d'emprunter trois mille ducats à l'usurier juif Shylock afin d'aider son ami
Bassanio à faire la conquête de la belle et riche héritière Portia. Certain de pouvoir le rembourser, il signe un contrat où il
autorise son créancier à lui prélever une livre de chair en cas de défaut de paiement. Il ne peut faire face à son échéance et
 Shylock, qui veut se venger des humiliations que lui ont fait subir les chrétiens, insiste pour que le contrat soit appliqué à la
lettre… Sur ce canevas, Shakespeare livre une de ces pièces les plus ambigüe. C'est celle où Shylock développe une des
plus belles tirades jamais écrite sur la tolérance: "Un Juif n'a-t-il pas des yeux ? (…) Si vous nous piquez, ne saignons-nous
 pas ? Si vous nous chatouillez, ne rions-nous pas ? Si vous nous empoisonnez, ne mourrons-nous pas ? Et si vous nous
bafouez, ne nous vengerons-nous pas ?"… Mais c'est aussi une pièce que certains voient comme antisémite.











