<p>

Orgon a recueilli chez lui un pauvre homme, Tartuffe, prêchant l’austérité et la piété. L'homme crée la division entre les
'tartuffiés' (Orgon et sa mère), et ceux qui résistent, pour différentes raisons et de différentes façons, à la prise de pouvoir
croissantes sur toute chose de celui en qui ils voient un faux dévot. L'intrigue de la pièce tourne autour de ces jeux de
pouvoirs, sur différentes tentatives de détruire l'emprise de Tartuffe sur Orgon, et sur les raisons de la complexité des
mécanismes de la relation entre Orgon et Tartuffe. Tout l'intérêt de l'interprétation de la compagnie de l'élan et de cette
mise en scène est de rendre justice à cette complexité, en faisant sortir Tartuffe de la caricature et en rendant enfin
compréhensible son authenticité et par là-même la fascination qu'il exerce sur Orgon.












