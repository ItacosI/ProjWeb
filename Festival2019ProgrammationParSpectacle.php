Peut-être sommes-nous tous des comédiens qui s'ignorent". En politique, en amitié, en
amour… partout nous faisons du théâtre. Écrite à quatre mains par Hadrien Brachet et
Pauline Mornet, étudiants à Sciences Po, "Titre Provisoire" explore la frontière (souvent très
fine) entre performance et réalité et s'interroge sur l'acte de création théâtrale. 15 saynètes se
succèdent, mêlant tous les genres, de la tragédie à la comédie en passant par l'absurde.
Chacune présente une situation et des personnages différents, soulignant l'omniprésence du
théâtre à tous les niveaux de la société et de nos vies. Les saynètes sont regroupées au sein de
trois grandes parties : "La Scène" qui se concentre sur l'univers du théâtre et l'expérience
d'acteur, "L'Autre" qui s'intéresse au théâtre dans les relations sociales et "La Politique" qui
explore les liens étroits entre théâtre, pouvoir et engagement politique. On pourra ainsi
croiser tout au long de la pièce une femme politique, un dramaturge vieillissant, une famille
autour d'un barbecue ou encore des hôtesses de l'air.







