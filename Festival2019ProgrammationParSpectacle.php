<p>

Port racines, magie d'un voyage immobile.Il n'est pas facile de qualifier le spectacle Port racines tant son écriture, son
esthétique et ses personnages sortent de l'ordinaire et ne répondent à aucun genre habituel. Conte ? Fable ? Récit ?
Parfois chantée, un peu dansée, l'écriture, à la fois poétique et crue provoque un foisonnement d'images. Rêvées ou
réelles ? On ne sait pas, et on s'en moque car on est très loin du réalisme. Et pourtant on y parle de la mer et ses
naufrages, d'exode, de pollution, de radioactivité, de guerre, de misère, de violence et de domination masculine. Autant de
sujets atemporels et constants à l'ombre du fantôme de Jacques Prévert. L'histoire est racontée par trois personnages,
bloqués dans un port déserté du Cotentin : la vieille Bobette, Lanaïa, femme-enfant déposée par la mer et le très charmant
Handive arrivé de la lande, bavard et mythomane qui cherche l'aventure mais aussi l'amour. Ces trois êtres fantasques
mêlent leur trois paroles et évoluent avec grâce dans un fouillis de textiles aux couleurs chatoyantes, tirés d'un tas de "sacs
Tati" qui symbolisent deux désirs contradictoires, l'appel d'un ailleurs, et la force des racines. Le spectateur enchanté, (au
sens fort du terme), par la richesse du texte et la beauté des images, a également applaudi l'agilité, la grâce, et la
générosité de ces trois artistes. » Catherine Reynaud - La Nouvelle République.







